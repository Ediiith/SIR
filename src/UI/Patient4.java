    private Genre toGenre(String s) {
        Genre genre = null;
        if (s.compareTo("Homme") == 0) {
            genre = Genre.HOMME;
        } else if (s.compareTo("Femme") == 0) {
            genre = Genre.FEMME;
        } else if (s.compareTo("Autre") == 0) {
            genre = Genre.AUTRE;
        }
        return genre;
    }
